"""Synthetic random-vs-random battle generator with production-quality teams.

Pipeline:
    1. Load pre-generated PS teams from data/team_pools/gen{N}randombattle.jsonl
       (generated by ps-server-gen9/gen_teams.js — genuine PS random battle teams
       with correct levels, role-based movesets, guaranteed type coverage).
    2. Run battles with poke-engine, sampling stochastic branches.
    3. Encode states from each player's partial-observability perspective:
       - Opponent species revealed only as they switch in / use moves.
       - Moves revealed only when used; item/ability only when triggered.
       - HP, stat boosts, status, volatile effects synced from engine every turn.
    4. Label outcomes with Foul Play eval on timeout; ±1 on game-end.
"""
