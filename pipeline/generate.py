MAX_TURNS = 100  # Raised from 50 — captures endgame hazard/status/PP dynamics


def run_random_battle(
    pool: dict[str, Any],
    vocab: Vocabulary,
    gen: int,
    rng: random.Random,
    pools_dir: str = "data/team_pools",
) -> list[TrainingSample]:
    poke_engine = _load_poke_engine()
    team1 = _sample_team(gen, rng, pools_dir)
    team2 = _sample_team(gen, rng, pools_dir)
    if team1:
        team1[0]["is_active"] = True
    if team2:
        team2[0]["is_active"] = True
    state = teams_to_poke_engine_state(team1, team2, gen)

    active_p1 = int(state.side_one.active_index)
    active_p2 = int(state.side_two.active_index)

    # P1 observes P2's active species; P2 observes P1's active species.
    obs_p1 = BattleObservationState.initial(active_p2)
    obs_p2 = BattleObservationState.initial(active_p1)

    samples_p1: list[tuple[Any, int, int]] = []
    samples_p2: list[tuple[Any, int, int]] = []

    for turn in range(MAX_TURNS):
        if is_terminal(state):
            break

        p1_moves = get_available_engine_moves(state.side_one)
        p2_moves = get_available_engine_moves(state.side_two)
        if not p1_moves or not p2_moves:
            break

        p1_choice = rng.choice(p1_moves)
        p2_choice = rng.choice(p2_moves)

        # Active slots BEFORE this turn (for correct observation updates)
        active_p1_slot = int(state.side_one.active_index)
        active_p2_slot = int(state.side_two.active_index)
        p1_move, p1_is_switch = _moves_from_choice(p1_choice)
        p2_move, p2_is_switch = _moves_from_choice(p2_choice)

        # Compute context counts for field encoding
        unrevealed_p2_for_p1 = max(0, 6 - len(obs_p1.revealed_slots))
        fainted_p2_for_p1    = len(obs_p1.fainted_slots)
        unrevealed_p1_for_p2 = max(0, 6 - len(obs_p2.revealed_slots))
        fainted_p1_for_p2    = len(obs_p2.fainted_slots)
        fainted_p1_own       = sum(1 for m in team1 if m.get("hp_fraction", 1.0) <= 0.0)
        fainted_p2_own       = sum(1 for m in team2 if m.get("hp_fraction", 1.0) <= 0.0)
        sleep_clause_p1      = any(m.get("status") == "slp" for m in team1)
        sleep_clause_p2      = any(m.get("status") == "slp" for m in team2)

        # Encode states from each player's partial-info perspective
        masked_opp_for_p1 = mask_opponent_for_p1(team2, obs_p1, active_p2_slot)
        masked_opp_for_p2 = mask_opponent_for_p1(team1, obs_p2, active_p1_slot)

        encoded_p1 = _encode_partial_state(
            team1, masked_opp_for_p1, p1_moves, turn, gen, vocab,
            unrevealed_p2=unrevealed_p2_for_p1,
            fainted_p2=fainted_p2_for_p1,
            unrevealed_p1=0,
            fainted_p1=fainted_p1_own,
            sleep_clause_p1=sleep_clause_p1,
            sleep_clause_p2=sleep_clause_p2,
        )
        encoded_p2 = _encode_partial_state(
            team2, masked_opp_for_p2, p2_moves, turn, gen, vocab,
            unrevealed_p2=unrevealed_p1_for_p2,
            fainted_p2=fainted_p1_for_p2,
            unrevealed_p1=0,
            fainted_p1=fainted_p2_own,
            sleep_clause_p1=sleep_clause_p2,
            sleep_clause_p2=sleep_clause_p1,
        )

        action_p1 = engine_move_to_action_index(state.side_one, p1_choice)
        action_p2 = engine_move_to_action_index(state.side_two, p2_choice)

        samples_p1.append((encoded_p1, action_p1, turn))
        samples_p2.append((encoded_p2, action_p2, turn))

        # Remember which mons were alive before applying this turn
        alive_p1_pre = _alive_slots(state.side_one)
        alive_p2_pre = _alive_slots(state.side_two)

        # Apply the selected branch
        try:
            branches = poke_engine.generate_instructions(state, p1_choice, p2_choice)
        except Exception:
            p1_choice = rng.choice(move_only_choices(state.side_one))
            p2_choice = rng.choice(move_only_choices(state.side_two))
            try:
                branches = poke_engine.generate_instructions(state, p1_choice, p2_choice)
                p1_move, p1_is_switch = _moves_from_choice(p1_choice)
                p2_move, p2_is_switch = _moves_from_choice(p2_choice)
            except Exception:
                break
        if not branches:
            break

        weights = [max(0.0, float(getattr(b, "percentage", 0.0))) for b in branches]
        branch = rng.choices(branches, weights=weights if sum(weights) > 0 else None, k=1)[0]
        state = state.apply_instructions(branch)

        # --- Sync full engine state into team dicts ---
        _sync_pokemon_state_from_engine(team1, state.side_one)
        _sync_pokemon_state_from_engine(team2, state.side_two)

        # --- Track last move used (enables Choice lock signal) ---
        if not p1_is_switch and p1_move:
            team1[active_p1_slot]["last_move"] = p1_move
        if not p2_is_switch and p2_move:
            team2[active_p2_slot]["last_move"] = p2_move

        # --- Detect newly fainted mons and mark in observation ---
        for slot in alive_p1_pre - _alive_slots(state.side_one):
            obs_p2.update_after_faint(slot)
        for slot in alive_p2_pre - _alive_slots(state.side_two):
            obs_p1.update_after_faint(slot)

        # --- Update observation state (revealed moves / switches) ---
        if p2_is_switch:
            obs_p1.update_after_switch(int(state.side_two.active_index))
        else:
            obs_p1.update_after_move(active_p2_slot, p2_move)

        if p1_is_switch:
            obs_p2.update_after_switch(int(state.side_one.active_index))
        else:
            obs_p2.update_after_move(active_p1_slot, p1_move)

    # --- Outcome determination ---
    if not has_healthy_pokemon(state.side_two):
        outcome_p1 = 1.0
    elif not has_healthy_pokemon(state.side_one):
        outcome_p1 = -1.0
    else:
        # Timeout: use Foul Play static eval (HP + alive count)
        score = _foul_play_eval(team1, team2)
        outcome_p1 = 1.0 if score > 0 else (-1.0 if score < 0 else 0.0)

    battle_id = f"synthetic_gen{gen}_{rng.randint(0, 2**32)}"
    result: list[TrainingSample] = []
    for encoded, action, turn in samples_p1:
        result.append(TrainingSample(battle_id, turn, encoded, action, outcome_p1, {}, gen))
    for encoded, action, turn in samples_p2:
        result.append(TrainingSample(f"{battle_id}_p2", turn, encoded, action, -outcome_p1, {}, gen))
    return result


