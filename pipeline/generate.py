def teams_to_poke_engine_state(
    team1: list[dict[str, Any]],
    team2: list[dict[str, Any]],
    gen: int,
) -> Any:
    return battle_to_poke_engine_state({"own_team": team1, "opponent_team": team2, "generation": gen})


def has_healthy_pokemon(side: Any) -> bool:
    return any(getattr(mon, "hp", 0) > 0 for mon in list(getattr(side, "pokemon", []) or []))


def is_terminal(state: Any) -> bool:
    return not has_healthy_pokemon(state.side_one) or not has_healthy_pokemon(state.side_two)


def get_available_engine_moves(side: Any) -> list[str]:
    active = side.pokemon[int(side.active_index)]
    choices: list[str] = []
    for move in active.moves:
        if not move.disabled and move.id != "none" and move.pp > 0:
            choices.append(str(move.id))
    active_idx = int(side.active_index)
    for idx, mon in enumerate(side.pokemon):
        if idx != active_idx and mon.hp > 0:
            choices.append(f"switch {mon.id}")
    return choices or ["tackle"]


def move_only_choices(side: Any) -> list[str]:
    active = side.pokemon[int(side.active_index)]
    choices = [str(m.id) for m in active.moves if not m.disabled and m.id != "none" and m.pp > 0]
    return choices or ["tackle"]


def engine_move_to_action_index(side: Any, choice: str) -> int:
    active = side.pokemon[int(side.active_index)]
    if choice.startswith("switch "):
        target = choice.split(" ", 1)[1]
        active_idx = int(side.active_index)
        switches = [mon.id for idx, mon in enumerate(side.pokemon) if idx != active_idx and mon.hp > 0]
        return 4 + switches.index(target) if target in switches[:5] else 4
    for idx, move in enumerate(active.moves[:4]):
        if str(move.id) == choice:
            return idx
    return 0


def _alive_slots(side: Any) -> set[int]:
    """Return indices of mons with hp > 0 in the engine state."""
    return {
        i for i, mon in enumerate(list(getattr(side, "pokemon", []) or []))
        if float(getattr(mon, "hp", 0)) > 0
    }


def _moves_from_choice(choice: str) -> tuple[str | None, bool]:
    """Returns (move_name, is_switch)."""
    if choice.startswith("switch "):
        return None, True
    return choice, False


# Status string normalisation from poke-engine to our canonical set
_ENGINE_STATUS_MAP: dict[str, str] = {
    "none": "none", "burn": "brn", "brn": "brn",
    "freeze": "frz", "frz": "frz",
    "paralysis": "par", "par": "par",
    "poison": "psn", "psn": "psn",
    "toxic": "tox", "tox": "tox",
    "sleep": "slp", "slp": "slp",
}


def _sync_pokemon_state_from_engine(team: list[dict[str, Any]], side: Any) -> None:
    """
    Sync all mutable per-Pokémon state from poke-engine after every turn.

    Covers: HP fraction, stat boosts (all 5), status condition, volatile effects,
    toxic counter, sleep turns, PP, and must_recharge flag.

    This is the only place that writes back engine state into the team dicts.
    Calling it every turn ensures encoded states reflect actual in-battle dynamics
    rather than the static initial values from team sampling.
    """
    eng_mons = list(getattr(side, "pokemon", []) or [])[:len(team)]
    for idx, eng_mon in enumerate(eng_mons):
        # --- HP ---
        hp = float(getattr(eng_mon, "hp", 0))
        maxhp = float(getattr(eng_mon, "maxhp", 1) or 1)
        team[idx]["hp_fraction"] = hp / max(1.0, maxhp)

        # --- Stat boosts ---
        team[idx]["boosts"] = {
            "atk": int(getattr(eng_mon, "attack_boost",           0) or 0),
            "def": int(getattr(eng_mon, "defense_boost",          0) or 0),
            "spa": int(getattr(eng_mon, "special_attack_boost",   0) or 0),
            "spd": int(getattr(eng_mon, "special_defense_boost",  0) or 0),
            "spe": int(getattr(eng_mon, "speed_boost",            0) or 0),
        }

        # --- Status condition ---
        raw_status = str(getattr(eng_mon, "status", "None") or "None").lower()
        raw_status = raw_status.replace("status.", "").replace("_", "")
        team[idx]["status"] = _ENGINE_STATUS_MAP.get(raw_status, "none")

        # --- Toxic counter & sleep turns ---
        team[idx]["toxic_count"] = int(getattr(eng_mon, "toxic_count", 0) or 0)
        team[idx]["sleep_turns"] = int(getattr(eng_mon, "sleep_turns", 0) or 0)

        # --- Volatile statuses ---
        volatiles_raw = getattr(eng_mon, "volatile_statuses", set()) or set()
        volatiles_list = [str(v).lower() for v in volatiles_raw]
        team[idx]["volatile_effects"] = volatiles_list
        team[idx]["must_recharge"] = "mustrecharge" in volatiles_list

        # --- PP ---
        eng_moves = list(getattr(eng_mon, "moves", []) or [])
        team[idx]["pp"] = [
            int(getattr(m, "pp", 35) or 35) for m in eng_moves[:4]
        ] + [35] * max(0, 4 - len(eng_moves))


def _foul_play_eval(team1: list[dict[str, Any]], team2: list[dict[str, Any]]) -> float:
    """
    Simplified Foul Play static evaluation for timeout outcome labelling.

    Uses POKEMON_ALIVE_STATIC=30 + POKEMON_HP=100 components from Foul Play
    evaluate.py (Scoring class). Unrevealed mons count as alive (conservative).
    """
    def score_side(team: list[dict[str, Any]]) -> float:
        s = 0.0
        for mon in team:
            hp = float(mon.get("hp_fraction", 0.0))
            if hp > 0.0:
                s += 30.0 + 100.0 * hp
        return s
    return score_side(team1) - score_side(team2)


def _encode_partial_state(
    own_team: list[dict[str, Any]],
    masked_opp: list[dict[str, Any] | None],
    available_choices: list[str],
    turn: int,
    gen: int,
    vocab: Vocabulary,
    *,
    unrevealed_p2: int = 5,
    fainted_p2: int = 0,
    unrevealed_p1: int = 0,
    fainted_p1: int = 0,
    sleep_clause_p1: bool = False,
    sleep_clause_p2: bool = False,
) -> Any:
    """Encode a state from one player's partial-information perspective."""
    move_choices   = [c for c in available_choices if not c.startswith("switch ")]
    switch_choices = [c for c in available_choices if c.startswith("switch ")]
    return encode_state(
        {
            "turn": turn,
            "generation": gen,
            "own_team": own_team,
            "opponent_team": masked_opp,
            "available_moves": [{"move": m, "disabled": False} for m in move_choices[:4]],
            "available_switches": [{"species": m.split(" ", 1)[1]} for m in switch_choices[:5]],
            "unrevealed_p1": unrevealed_p1,
            "unrevealed_p2": unrevealed_p2,
            "fainted_p1": fainted_p1,
            "fainted_p2": fainted_p2,
            "sleep_clause_p1": int(sleep_clause_p1),
            "sleep_clause_p2": int(sleep_clause_p2),
        },
        vocab=vocab,
        generation=gen,
    )


