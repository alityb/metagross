def _load_team_pool(gen: int, pools_dir: str) -> list[str]:
    """Load packed team strings for the given gen from the pre-generated pool."""
    key = f"gen{gen}"
    if key not in _TEAM_POOLS:
        path = Path(pools_dir) / f"gen{gen}randombattle.jsonl"
        if path.exists():
            _TEAM_POOLS[key] = [l.strip() for l in path.read_text().splitlines() if l.strip()]
        else:
            _TEAM_POOLS[key] = []
    return _TEAM_POOLS[key]


def unpack_ps_team(packed: str, gen: int) -> list[dict[str, Any]]:
    """
    Parse a PS packed team string into our team dict format.

    Packed format per Pokemon (separated by ']'):
        name|species|item|ability|moves|nature|evs|gender|ivs|shiny|level|...
    If 'species' is blank, 'name' is the species.
    """
    result: list[dict[str, Any]] = []
    for slot in packed.strip().split("]"):
        slot = slot.strip()
        if not slot:
            continue
        parts = slot.split("|")
        if len(parts) < 4:
            continue
        nickname = parts[0].strip()
        species_raw = parts[1].strip() if parts[1].strip() else nickname
        item = normalize_name(parts[2].strip()) if len(parts) > 2 else ""
        ability = normalize_name(parts[3].strip()) if len(parts) > 3 else ""
        moves_str = parts[4].strip() if len(parts) > 4 else ""
        moves = [normalize_name(m.strip()) for m in moves_str.split(",") if m.strip()][:4]
        level = 100
        if len(parts) > 10 and parts[10].strip().isdigit():
            level = int(parts[10].strip())
        result.append({
            "species": normalize_name(species_raw),
            "moves": moves,
            "item": item,
            "ability": ability,
            "level": level,
            "hp_fraction": 1.0,
            "is_active": False,
            "status": "none",
            "boosts": {},
            "volatile_effects": [],
            "toxic_count": 0,
            "sleep_turns": 0,
            "last_move": None,
            "must_recharge": False,
            "pp": [35, 35, 35, 35],
        })
    return result


def sample_team_from_pool(
    gen: int,
    rng: random.Random,
    pools_dir: str = "data/team_pools",
) -> list[dict[str, Any]] | None:
    """Sample a genuine PS random battle team from the pre-generated pool.

    Returns None if the pool is unavailable (fall back to sample_team_legacy).
    """
    pool = _load_team_pool(gen, pools_dir)
    if not pool:
        return None
    packed = rng.choice(pool)
    team = unpack_ps_team(packed, gen)
    if len(team) < 2:
        return None
    return team


