@dataclass
class BattleObservationState:
    """Tracks what one player has directly observed about the opponent."""
    revealed_slots: set[int]
    revealed_moves: dict[int, set[str]]
    revealed_items: dict[int, str | None]
    revealed_abilities: dict[int, str | None]
    last_move: dict[int, str | None]    # slot → last move used (Choice lock signal)
    fainted_slots: set[int]             # slots confirmed fainted (distinct from unrevealed)

    @classmethod
    def initial(cls, active_slot: int) -> "BattleObservationState":
        """Battle starts — active slot species revealed, nothing else known."""
        return cls(
            revealed_slots={active_slot},
            revealed_moves={active_slot: set()},
            revealed_items={},
            revealed_abilities={},
            last_move={},
            fainted_slots=set(),
        )

    def update_after_move(
        self,
        opp_active_slot: int,
        move_used: str | None,
        item_activated: str | None = None,
        ability_triggered: str | None = None,
    ) -> None:
        self.revealed_slots.add(opp_active_slot)
        self.revealed_moves.setdefault(opp_active_slot, set())
        if move_used:
            self.revealed_moves[opp_active_slot].add(move_used)
            self.last_move[opp_active_slot] = move_used
        if item_activated:
            self.revealed_items[opp_active_slot] = item_activated
        if ability_triggered:
            self.revealed_abilities[opp_active_slot] = ability_triggered

    def update_after_switch(self, new_active_slot: int) -> None:
        """Opponent switches in — new slot species now revealed."""
        self.revealed_slots.add(new_active_slot)
        self.revealed_moves.setdefault(new_active_slot, set())

    def update_after_faint(self, slot: int) -> None:
        """Opponent mon fainted — mark as revealed-and-fainted regardless of prior state."""
        self.revealed_slots.add(slot)
        self.fainted_slots.add(slot)


def mask_opponent_for_p1(
    full_opp_team: list[dict[str, Any]],
    obs: BattleObservationState,
    active_opp_slot: int,
) -> list[dict[str, Any] | None]:
    """
    Return the opponent team from the observing player's partial-information view.

    Unrevealed slots → None  (encode_state maps None to full-UNKNOWN token).
    Revealed slots   → subset of known info: species always visible once seen,
                       moves only those actually used, item/ability only if triggered.
    Fainted slots encode with hp_fraction=0.0 (distinct from UNKNOWN).
    last_move included so the value head can detect Choice-locked opponents.
    """
    visible: list[dict[str, Any] | None] = []
    for idx, mon in enumerate(full_opp_team):
        if idx not in obs.revealed_slots:
            visible.append(None)
        else:
            is_fainted = idx in obs.fainted_slots
            is_active = idx == active_opp_slot
            visible.append({
                "species": mon["species"],
                "moves": sorted(obs.revealed_moves.get(idx, set()))[:4],
                "item": obs.revealed_items.get(idx),
                "ability": obs.revealed_abilities.get(idx),
                "hp_fraction": 0.0 if is_fainted else mon.get("hp_fraction", 1.0),
                "is_active": is_active,
                "status": mon.get("status", "none"),
                "types": mon.get("types", []),
                "last_move": obs.last_move.get(idx),
                # Only expose live battle state for the active mon
                "boosts": mon.get("boosts", {}) if is_active else {},
                "volatile_effects": mon.get("volatile_effects", []) if is_active else [],
                "must_recharge": mon.get("must_recharge", False) if is_active else False,
            })
    return visible


